/**
 * Get an HTML element representing the input concept.
 *
 * @param {string} name The concept.
 * @returns {SVGSVGElement} The element.
 */
export function getIconElement(name) {
  const xmlns = 'http://www.w3.org/2000/svg';
  const path = document.createElementNS(xmlns, 'path');
  if (typeof icons[name] === 'undefined') {
    throw new Error('No icon with name: ' + name);
  }
  path.setAttributeNS(null, 'd', icons[name]);
  const svg = document.createElementNS(xmlns, 'svg');
  svg.setAttributeNS(null, 'height', 20);
  svg.setAttributeNS(null, 'width', 20);
  svg.setAttributeNS(null, 'viewBox', '0 -960 960 960');
  // avoid pointer events to let parent be target
  svg.style.pointerEvents = 'none';
  svg.appendChild(path);
  return svg;
}

/**
 * Get a button for a given name.
 *
 * @param {string} name The name of the button.
 * @returns {HTMLButtonElement} The element.
 */
export function getButton(name) {
  const button = document.createElement('button');
  button.title = name;
  button.name = name;
  button.appendChild(getIconElement(name));
  return button;
}

/**
 * Set a button as pressed or not.
 *
 * @param {HTMLButtonElement} button The button to set state.
 * @param {boolean} state True to set as pressed.
 */
export function setButtonPressed(button, state) {
  const name = button.name;
  if (state) {
    button.value = '1';
    button.replaceChildren(getIconElement(name + '_off'));
  } else {
    button.value = '0';
    button.replaceChildren(getIconElement(name));
  }
}

/**
 * Get a button pressed state.
 *
 * @param {HTMLButtonElement} button The button to get state.
 * @returns {boolean} True if pressed.
 */
export function isButtonPressed(button) {
  return button.value === '1';
}

/**
 * Toggle a button pressed state.
 *
 * @param {HTMLButtonElement} button The button to get state.
 */
export function toggleButtonPressed(button) {
  if (isButtonPressed(button)) {
    setButtonPressed(button, false);
  } else {
    setButtonPressed(button, true);
  }
}
